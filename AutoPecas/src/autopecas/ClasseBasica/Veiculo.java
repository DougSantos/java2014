package autopecas.ClasseBasica;

/**
 *
 * @author NeGo
 */
class Veiculo {
    
    
    private Integer codigoVeiculo;
    private Integer modelo;
    private String veiculo;
    private String cor;

    /**
     * @return the codigoVeiculo
     */
    public Integer getCodigoVeiculo() {
        return codigoVeiculo;
    }

    /**
     * @param codigoVeiculo the codigoVeiculo to set
     */
    public void setCodigoVeiculo(Integer codigoVeiculo) {
        this.codigoVeiculo = codigoVeiculo;
    }

    /**
     * @return the modelo
     */
    public Integer getModelo() {
        return modelo;
    }

    /**
     * @param modelo the modelo to set
     */
    public void setModelo(Integer modelo) {
        this.modelo = modelo;
    }

    /**
     * @return the veiculo
     */
    public String getVeiculo() {
        return veiculo;
    }

    /**
     * @param veiculo the veiculo to set
     */
    public void setVeiculo(String veiculo) {
        this.veiculo = veiculo;
    }

    /**
     * @return the cor
     */
    public String getCor() {
        return cor;
    }

    /**
     * @param cor the cor to set
     */
    public void setCor(String cor) {
        this.cor = cor;
    }
    
}
